#aritmatika

x=24
y=75

#Operasi Penjumlahan
hasil = x + y
print ("nilai dari x + y = ",hasil)

# Pengurangan
hasil = x - y
print("nilai dari x - y = ",hasil)

# Perkalian
hasil = x * y
print("nilai dari x * y = ",hasil)

# Pembagian
hasil = x / y
print("nilai dari x / y = ",hasil)

# eksponen (perpangkatan)
hasil = x ** y
print("nilai dari x ** y = ",hasil)

# Modulus (sisa bagi)
hasil = x % y
print("nilai dari x Mod y = ",hasil)

# Floor Division
hasil= x // y
print("nilai dari x FD y = ",hasil)